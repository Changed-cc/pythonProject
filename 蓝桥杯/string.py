str = 'Hello,Warld!'
print(str.swapcase())#所有字母的大小写反转
print(str.upper())#所有字母大写
print(str.lower())#所有字母小写
print(str.isdigit())#如果字符串中的所有字符都是数字，并且至少有一个字符，返回 True ，否则返回 False
print(str.isnumeric())#如果字符串中至少有一个字符且所有字符均为数值字符则返回 True ，否则返回 False
print(str.capitalize())#创建字符串副本，但首字母大写
print(str.isalpha())#字符是否都是字母
print(str.isalnum())#字符是否都是字母或数
print(str.isspace())#字符是否都是空白字符